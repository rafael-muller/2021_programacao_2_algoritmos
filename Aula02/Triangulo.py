class Triangulo:
    def __init__(self, altura, largura):
        self.altura = altura
        self.largura = largura

    def setAltura(self, altura):
        self.altura = altura

    def setLargura(self, largura):
        self.largura = largura

    def getAltura(self):
        return self.altura

    def getLargura(self):
        return self.largura 